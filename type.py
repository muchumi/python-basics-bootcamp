# List
chores = ['Brush teeth', 'Bath', 'Prepare Breakfast', 'Pertake Breakfast']

print(type(chores)) # list

# Dictionary
student = {
    "name": "Maurice Maina",
    "age": "7",
    "yearOfBirth": "2034"
}

print(type(student)) # dict

# Arithmetic Operations
numOne = 23
numTwo = 54
numThree = numOne + numTwo
print(numThree, type(numThree)) # 77 <class 'int'>
