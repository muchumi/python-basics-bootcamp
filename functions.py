def hello_world(job):
    if job == 'Software Engineer':
        print('He is a Software Engineer')
    elif job == 'Product Analyst':
        print('She is a Product Analyst')
    elif job == 'Software Quality Assurance Analyst':
        print('She is a Software Quality Assurance Analyst')
    else:
        print('Unidentified job title')

hello_world('Software Engineer')
hello_world('Software Quality Assurance Analyst')
hello_world('Product Analyst')
hello_world('Information Technology Support')

