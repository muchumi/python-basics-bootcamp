greetings = "  Hello there  "
# Remove trailing whitespace at the beginning of the string
print(greetings.lstrip()) # Result: "Hello there  "
# Remove trailing whitespace at the end of the string
print(greetings.rstrip()) # Result: "  Hello there"
# Remove whitespaces at the beginning and at the end
print(greetings.strip()) # Result: "Hello there"


